local function checkReq(type, value)
	if not type then return false end
	if not value then return false end

	if type == "lvl" or type == "passlvl" then
		return UnitLevel("player") >= tonumber(value)
	elseif type == "notlvl" then
		return UnitLevel("player") < tonumber(value)
	elseif type == "qid" then
		local qid = tonumber(value)
		if qid then
			return C_QuestLog.IsQuestFlaggedCompleted(qid)
		end
	elseif type == "nqid" then
		local qid = tonumber(value)
		if qid then
			return not C_QuestLog.IsQuestFlaggedCompleted(qid)
		end
	elseif type == "fac" then
		return UnitFactionGroup("player") == tostring(value)
	elseif type == "cls" then
		return select(2, UnitClass("player")) == value
	elseif type == "rac" then
		return select(2, UnitRace("player")) == value
	elseif type == "map" then
		local playerMap = C_Map.GetBestMapForUnit("player")
		return playerMap == tonumber(value)
	elseif type == "spell" then
		local spellName = GetSpellInfo(tonumber(value))
		if spellName then
			if GetSpellBookItemInfo(spellName) then
				return true
			end
		end
		return false
	end
	return true
end

local HBD = LibStub("HereBeDragons-2.0")

local coordKeys = {
	["toX"] = true,
	["toY"] = true,
	["fromX"] = true,
	["fromY"] = true,
	["toMapId"] = true,
	["fromMapId"] = true,
}


local function getNodes(d)
	local _, _, c1 = HBD:GetWorldCoordinatesFromZone(d.toX, d.toY, d.toMapId)
	local n1 = {
		["x"] = d.toX,
		["y"] = d.toY,
		["mapId"] = d.toMapId,
		["continent"] = c1
	}
	local n2

	local hasFrom = d.fromMapId and d.fromX and d.fromY
	if hasFrom then
		local _, _, c2 = HBD:GetWorldCoordinatesFromZone(d.fromX, d.fromY, d.fromMapId)
		n2 = {
			["x"] = d.fromX,
			["y"] = d.fromY,
			["mapId"] = d.fromMapId,
			["continent"] = c2
		}
	end
	for i, j in pairs(d) do
		if not coordKeys[i] then
			if hasFrom then
				n2[i] = j
			end
			n1[i] = j
		end
	end
	return n1, n2
end

function module:OnInitialize()
	core.NavigationData = {}
	local index = 0
	local linkIndex = 0
	for _, data in ipairs(NavigationData) do
		local reqs = data.reqs
		if reqs then
			local validNode = false
			for rKey, rValue in pairs(reqs) do
				validNode = checkReq(rKey, rValue)
			end
			if validNode then
				local n1, n2 = getNodes(data)
				if n1 then
					core.NavigationData[index] = n1
					core.NavigationData[index].link = -1
					index = index + 1
				end
				if n2 then
					linkIndex = linkIndex + 1
					core.NavigationData[index - 1].link = linkIndex
					n2.link = linkIndex
					core.NavigationData[index] = n2
					index = index + 1
				end
			end
		else
			local n1, n2 = getNodes(data)
			if n1 then
				core.NavigationData[index] = n1
				core.NavigationData[index].link = -1
				index = index + 1
			end
			if n2 then
				linkIndex = linkIndex + 1
				core.NavigationData[index - 1].link = linkIndex
				n2.link = linkIndex
				core.NavigationData[index] = n2
				index = index + 1
			end
		end
	end
end
